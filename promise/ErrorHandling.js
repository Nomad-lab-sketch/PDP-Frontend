// При возникновении ошибки – она отправляется в ближайший обработчик onRejected.

// Если ошибка происходит в блоке then, то мы попадаем в блок catch. 
// Однако, если ошибка происходит в самом Promise до того, как будет выполнен блок then, 
// то мы также попадаем в блок catch. Например:


new Promise((resolve, reject) => {
	// здесь может быть код, который вызовет ошибку
	throw new Error('Ошибка в Promise');
})
	.then(() => {
		console.log('Этот блок не выполнится');
	})
	.catch(error => {
		console.log(error.message); // выведет "Ошибка в Promise"
	});


// В этом примере мы сразу выбрасываем ошибку в самом Promise и попадаем в блок catch. 
// Блок then не выполнится, так как перед ним произошла ошибка.